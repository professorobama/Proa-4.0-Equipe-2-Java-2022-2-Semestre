package equipe02;

public class OperadoresUnarios4 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 5;
		double resultado = 10/Math.pow(2,2 );
		
		System.out.println("A adição é :" + (a+b));
		System.out.println("A subtração é :" + (a-b));
		System.out.println("A multiplicação é :" + (a*b));
		System.out.println("A divisão é :" + (a/b));
		System.out.println("A resto da divisão é :" + (a%b));
		System.out.println(10<<2); //10* 2² = 10* 4 =40
		System.out.println(10<<4); //10*
		System.out.println(20<<3);
		System.out.println("Resultado é :"+ resultado);
	}

}
