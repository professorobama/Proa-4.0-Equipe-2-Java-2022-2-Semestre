package equipe02;

public class TipoDeDados {

	public static void main(String[] args) {
		
		char letra ='a';
		System.out.println(letra);
		
		byte numero = 127;
		byte numero1= -128;
		System.out.println(numero);
		System.out.println(numero1);
		
		short numero2=-32768;
		short numero3=32767;
		System.out.println(numero2);
		System.out.println(numero3);
		
		int numero4=-214748368;
		int numero5=2147483647;
		System.out.println(numero4);
		System.out.println(numero5);
		
		long numero6=-9223372036854775808L;
		long numero7=9223372036854775807L;
		System.out.println(numero6);
		System.out.println(numero7);
		
		float numero8=-9223372036854775808f;
		float numero9=92233720368.54775807f;
		System.out.println(numero8);
		System.out.println(numero9);
		
		double numero10=-9223372036854775808L;
		double numero11=9223372036854775807L;
		System.out.println(numero10);
		System.out.println(numero11);

		String nome = "Jailson";
		System.out.println(nome);
	}

}
